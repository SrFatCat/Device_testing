#!/bin/bash
. ./pin_on_off.sh
. ./dialog_yes_no.sh

function camera1 {
if [[ $1 == *"1"* ]]
then
off 6
yesno "Проверка ключей камер" "Подключите крайний левый ключ камеры. \n\nСветодиод потух?" "Крайний левый ключ камеры: работает" "Крайний левый ключ камеры: не работает"
on 6
fi
}

function camera2 {
if [[ $1 == *"2"* ]]
then
off 13
yesno "Проверка ключей камер" "Подключите средний ключ камеры. \n\nСветодиод потух?" "Средний ключ камеры: работает" "Средний ключ камеры: не работает"
on 13
fi
}

function camera3 {
if [[ $1 == *"3"* ]]
then
off 26
yesno "Проверка ключей камер" "Подключите крайний правый ключ камеры. \n\nСветодиод потух?" "Крайний правый ключ камеры: работает" "Крайний правый ключ камеры: не работает"
on 26
fi
}

function cameras {
cam_key=$(whiptail --title "Проверка ключей камер" --inputbox "Введите какие из ключей камер есть на плате" 10 60 3>&1 1>&2 2>&3)
exitstatus=$?
if [ $exitstatus = 0 ];
then
        camera1 "$cam_key"
        camera2 "$cam_key"
        camera3 "$cam_key"
else
        echo "Отказ от ввода"
fi
}


